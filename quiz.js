let currentTest = 0
let step = 0
let answers = []
const quizStage = document.getElementById('quizStage')

function slideRender(html) {
	quizStage.classList.remove('card-animate')
	quizStage.innerHTML = html
	void quizStage.offsetWidth
	quizStage.classList.add('card-animate')
}

function renderQuestions(withAnim = true) {
	const quizData = testBanks[currentTest].quizData
	const q = quizData[step]
	let html = `
    <h2>${q.question}</h2>
    <div id="options">
      ${q.options
				.map(
					(opt, idx) => `
        <label class="option-label${
					answers[step]?.includes(idx) ? ' selected' : ''
				}">
          <input type="checkbox" class="option-checkbox" name="option"
            ${answers[step]?.includes(idx) ? 'checked' : ''}
            onchange="toggleChoice(${idx})">
          <span>${opt.text}</span>
        </label>
      `
				)
				.join('')}
    </div>
    <div class="quiz-footer">
      <span class="quiz-step">Шаг: ${step + 1}/${quizData.length}</span>
      <span class="quiz-controls">
        <button type="button" class="btn" onclick="prevStep()" ${
					step === 0 ? 'disabled' : ''
				}>&#8592;</button>
        <button type="button" class="btn btn-main" onclick="nextStep()" ${
					!answers[step] || answers[step].length === 0 ? 'disabled' : ''
				}>
          ${step < quizData.length - 1 ? 'Далее' : 'Результат'}
        </button>
      </span>
    </div>
  `
	if (withAnim) slideRender(html)
	else quizStage.innerHTML = html
}
window.toggleChoice = function (idx) {
	if (!answers[step]) answers[step] = []
	if (answers[step].includes(idx)) {
		answers[step] = answers[step].filter((i) => i !== idx)
	} else {
		answers[step].push(idx)
	}
	renderQuestions(false)
}
window.prevStep = function () {
	if (step > 0) step--
	renderQuestions(true)
}
window.nextStep = function () {
	const quizData = testBanks[currentTest].quizData
	if (step < quizData.length - 1) {
		step++
		renderQuestions(true)
	} else {
		renderResult()
	}
}
function renderResult() {
	const quizData = testBanks[currentTest].quizData
	let stats = {},
		total = 0
	quizData.forEach((q, qi) => {
		if (!answers[qi]) return
		answers[qi].forEach((idx) => {
			let opt = q.options[idx]
			let roles = Array.isArray(opt.role) ? opt.role : [opt.role]
			roles.forEach((role) => {
				stats[role] = (stats[role] || 0) + 1
			})
			total += 1
		})
	})
	let topRoles = Object.entries(stats)
		.sort((a, b) => b[1] - a[1])
		.slice(0, 3)
	let resultData = {
		roles: topRoles,
		total,
		timestamp: Date.now(),
	}
	saveResult(currentTest, resultData)

	renderSwiperResult(resultData)
}
